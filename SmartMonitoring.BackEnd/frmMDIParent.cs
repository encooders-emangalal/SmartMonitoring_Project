        private void toolStripButton1_Click(object sender, EventArgs e)
        {
            Form _form1 = Application.OpenForms.Cast<Form>().Where(p => p.Name == "frmMain").FirstOrDefault();
            if (_form1 == null)
            {
                frmMain _frm1 = new frmMain();
                _frm1.MdiParent = this;
                _frm1.Show();
            }
            else
            {
                _form1.Show();
                _form1.Focus();
            }

